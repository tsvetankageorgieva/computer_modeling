# Намиране на броя на цифрите на цяло число
import random

# Получаване на цяло число, генерирано по случаен начин
number = random.randint(-100000000, 100000000)

# преобразуване на числото в низ и намиране на неговата дължина
digit_count = len(str(number))

# Извеждане на броя на цифрите на въведеното число
print("Числото е", number)
print("Броят на цифрите на числото е", digit_count)

