# Изчертаване на координатна система
import turtle
pen = turtle.Turtle()
pen.hideturtle()
# Изчертаване на Ox
pen.penup()
pen.goto(-200, 0)
pen.pendown()
pen.goto(200, 0)
pen.write("x", align="left")
pen.penup()
# Изчертаване на стрелката на Ox
pen.goto(190,5)
pen.pendown()
pen.goto(200,0)
pen.penup()
pen.goto(190,-5)
pen.pendown()
pen.goto(200,0)
# Изчертаване на Oy
pen.penup()
pen.goto(0, -200)
pen.pendown()
pen.goto(0, 200)
pen.write("y", align="right")
# Изчертаване на стрелката на Oy
pen.goto(5,190)
pen.pendown()
pen.goto(0,200)
pen.penup()
pen.goto(-5,190)
pen.pendown()
pen.goto(0,200)
# Изписване на O
pen.penup()
pen.goto(-3,-18)
pen.write("O", align="right")
# Изчертаване на деления по осите
for i in range(1, 20):
    pen.penup()
    pen.goto(-(200-i*20), 3)
    pen.pendown()
    pen.goto(-(200-i*20), -3)
for i in range(1, 20):
    pen.penup()
    pen.goto(3, -(200-i*20))
    pen.pendown()
    pen.goto(-3, -(200-i*20))

# Изчертаване точките A(-3; -3), B(3; -3), C(3; 2), D(0;4), E(-3; 1) и свързването им във фигура.
pen.penup()
pen.goto(-3*20, -3*20)
pen.pendown()
pen.circle(0.5)
pen.write("A", align="right")
pen.penup()
pen.goto(3*20, -3*20)
pen.pendown()
pen.circle(0.5)
pen.write(" B", align="left")
pen.penup()
pen.goto(3*20, 2*20)
pen.pendown()
pen.circle(0.5)
pen.write(" C", align="left")
pen.penup()
pen.goto(0*20, 4*20)
pen.pendown()
pen.circle(0.5)
pen.write("D", align="right")
pen.penup()
pen.goto(-3*20, 1*20)
pen.pendown()
pen.circle(0.5)
pen.write("E", align="right")
# Свързване на точките
pen.penup()
pen.goto(-3*20, -3*20)
pen.pendown()
pen.goto(3*20, -3*20)
pen.goto(3*20, 2*20)
pen.goto(0*20, 4*20)
pen.goto(-3*20, 1*20)
pen.goto(-3*20, -3*20)
# Оцветяване на допълващите триъгълници
pen.penup()
pen.fillcolor("green")
pen.goto(3*20, 2*20)
pen.begin_fill()
pen.goto(3*20, 4*20)
pen.goto(0*20, 4*20)
pen.end_fill()
pen.begin_fill()
pen.goto(-3*20, 4*20)
pen.goto(-3*20, 1*20)
pen.end_fill()

